// Helper: get all lessons flat
export const getAllLessons = () => TOPICS.flatMap((t) => t.lessons);

// Helper: find lesson by id, merging in difficulty-tagged extra steps
export const getLessonById = (id: string) => {
  const lesson = getAllLessons().find((l) => l.id === id);
  if (!lesson) return undefined;
  const extra = getExtraSteps(id);
  if (extra.length === 0) return lesson;
  return {
    ...lesson,
    steps: [...lesson.steps, ...extra].sort((a, b) => a.order_index - b.order_index),
  };
};

// Helper: get topic by lesson id
export const getTopicByLessonId = (lessonId: string) =>
  TOPICS.find((t) => t.lessons.some((l) => l.id === lessonId));
